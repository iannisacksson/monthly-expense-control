import type { IIncomeTaxRepository } from "../../../domain/repositories/income-tax.repository";
import type { IMonthlyIncomeRepository } from "../../../domain/repositories/monthly-income.repository";
import {
  ForbiddenError,
  NotFoundError,
  UnprocessableEntityError,
} from "../../../utils/errors";
import { User } from "../../../domain/entities/user.entity";
import {
  IncomeTax,
  IncomeTaxEntity,
} from "../../../domain/entities/income-tax.entity";

export class CreateIncomeTaxUseCase {
  constructor(
    private readonly incomeTaxRepository: IIncomeTaxRepository,
    private readonly monthlyIncomeRepository: IMonthlyIncomeRepository,
  ) {}

  async execute(data: IncomeTax, requestingUser: User): Promise<IncomeTax> {
    if (!data.taxType || data.taxType.length === 0) {
      throw new UnprocessableEntityError("Tax type is required");
    }

    if (data.value < 0) {
      throw new UnprocessableEntityError(
        "Tax value must be greater than or equal to zero",
      );
    }

    const income = await this.monthlyIncomeRepository.findById(
      data.monthlyIncome.id,
    );
    if (!income) {
      throw new NotFoundError("Monthly income not found");
    }

    if (income.user.id !== requestingUser.id) {
      throw new ForbiddenError();
    }

    if (data.isAuto) {
      throw new UnprocessableEntityError(
        "Automatic income taxes must be generated by the income service",
      );
    }

    return this.incomeTaxRepository.create(
      new IncomeTaxEntity({
        monthlyIncome: data.monthlyIncome,
        taxType: data.taxType,
        value: data.value,
        isAuto: false,
      }),
    );
  }
}
